function getItemsFromSuperObj(obj,ans={}){
  for(const key in obj){
    const isFolder = !obj[key].currentAng; //currentAng will never be 0
    if(isFolder){
      ans[key]="folder"
      getItemsFromSuperObj(obj[key],ans)
    }else{
      ans[key]=obj[key].uri
    }
  }
  return ans
}
